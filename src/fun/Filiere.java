package fun;

public class Filiere {
	
	private int idFiliere;
	private String nom;

	public Filiere() {
	}
	
	public void setIdFiliere(int idFiliere) {
		this.idFiliere = idFiliere;
	}
	
	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getIdFiliere() {
		return idFiliere;
	}

	public String getNom() {
		return nom;
	}

}